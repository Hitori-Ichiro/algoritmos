numeroDia = int(input("Ingrese un número entre 1 y 7: "))
if numeroDia == 1:
    print("Lunes")
elif numeroDia == 2:
    print("Martes")
elif numeroDia == 3:
    print("Miércoles")
elif numeroDia == 4:
    print("Jueves")
elif numeroDia == 5:
    print("Viernes")
elif numeroDia == 6:
    print("Sábado")
elif numeroDia == 7:
    print("Domingo")